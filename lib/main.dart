class HomePage extends StatefulWidget {
  const HomePage({Key? key}) : super(key: key);

  @override
  State<HomePage> createState() => _HomePageState();
}

class _HomePageState extends State<HomePage> {
  TextEditingController jobController = TextEditingController();

  @override
  Widget build(BuildContext context) {
    final authService = context.read<AuthService>();
    final user = authService.currentUser()!;
    return Consumer<BucketService>(
      builder: (context, bucketService, child) {
        return Scaffold(
          appBar: AppBar(
            title: Text("Î≤ÑÌÇ∑ Î¶¨Ïä§Ìä∏"),
            actions: [
              TextButton(
                child: Text(
                  "Î°úÍ∑∏ÏïÑÏõÉ",
                  style: TextStyle(
                    color: Colors.white,
                  ),
                ),
                onPressed: () {
                  print("sign out");
                  // Î°úÍ∑∏ÏïÑÏõÉ
                  context.read<AuthService>().signOut();
                  Navigator.pushReplacement(
                    context,
                    MaterialPageRoute(builder: (_) => LoginPage()),
                  );
                },
              ),
            ],
          ),
          body: Column(
            children: [
              /// ÏûÖÎ†•Ï∞Ω
              Padding(
                padding: const EdgeInsets.all(8),
                child: Row(
                  children: [
                    /// ÌÖçÏä§Ìä∏ ÏûÖÎ†•Ï∞Ω
                    Expanded(
                      child: TextField(
                        controller: jobController,
                        decoration: InputDecoration(
                          hintText: "ÌïòÍ≥† Ïã∂ÏùÄ ÏùºÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.",
                        ),
                      ),
                    ),

                    /// Ï∂îÍ∞Ä Î≤ÑÌäº
                    ElevatedButton(
                      child: Icon(Icons.add),
                      onPressed: () {
                        // create bucket
                        if (jobController.text.isNotEmpty) {
                          print("create bucket");
                          bucketService.create(jobController.text, user.uid);
                        }
                      },
                    ),
                  ],
                ),
              ),
              Divider(height: 1),

              /// Î≤ÑÌÇ∑ Î¶¨Ïä§Ìä∏
              Expanded(
                child: FutureBuilder<QuerySnapshot>(
                    future: bucketService.read(user.uid),
                    builder: (context, snapshot) {
                      final docs = snapshot.data?.docs ?? []; // Î¨∏ÏÑúÎì§ Í∞ÄÏ†∏Ïò§Í∏∞
                      if (docs.isEmpty) {
                        return Center(child: Text("Î≤ÑÌÇ∑ Î¶¨Ïä§Ìä∏Î•º ÏûëÏÑ±Ìï¥Ï£ºÏÑ∏Ïöî."));
                      }
                      return ListView.builder(
                        itemCount: docs.length,
                        itemBuilder: (context, index) {
                          final doc = docs[index];
                          String job = doc.get('job');
                          bool isDone = doc.get('isDone');
                          return ListTile(
                            title: Text(
                              job,
                              style: TextStyle(
                                fontSize: 24,
                                color: isDone ? Colors.grey : Colors.black,
                                decoration: isDone
                                    ? TextDecoration.lineThrough
                                    : TextDecoration.none,
                              ),
                            ),
                            // ÏÇ≠Ï†ú ÏïÑÏù¥ÏΩò Î≤ÑÌäº
                            trailing: IconButton(
                              icon: Icon(CupertinoIcons.delete),
                              onPressed: () {
                                // ÏÇ≠Ï†ú Î≤ÑÌäº ÌÅ¥Î¶≠Ïãú
                                bucketService.delete(doc.id);
                              },
                            ),
                            onTap: () {
                              // ÏïÑÏù¥ÌÖú ÌÅ¥Î¶≠ÌïòÏó¨ isDone ÏóÖÎç∞Ïù¥Ìä∏
                              bucketService.update(doc.id, !isDone);
                            },
                          );
                        },
                      );
                    }),
              ),
            ],
          ),
        );
      },
    );
  }
}
